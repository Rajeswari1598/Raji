package methodsNov7day4;

public class Person {
   private String pname;
   private int age;
public Person() {
	super();
}
public Person(String pname, int age) {
	super();
	this.pname = pname;
	this.age = age;
}
public String getPname() {
	return pname;
}
public void setPname(String pname) {
	this.pname = pname;
}
public int getAge() {
	return age;
}
public void setAge(int age) {
	this.age = age;
}
}
